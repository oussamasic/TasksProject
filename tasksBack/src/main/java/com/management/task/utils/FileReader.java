package com.management.task.utils;

import com.management.task.service.SendMailService;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;

public class FileReader {

    private FileReader() {
        throw new IllegalStateException("Utility class");
    }

    private static String readFromInputStream(InputStream inputStream)
            throws IOException {
        StringBuilder resultStringBuilder = new StringBuilder();
        try (BufferedReader br
                     = new BufferedReader(new InputStreamReader(inputStream))) {
            String line;
            while ((line = br.readLine()) != null) {
                resultStringBuilder.append(line).append("\n");
            }
        }
        return resultStringBuilder.toString();
    }

    public static String loadFileContent(String filename) throws IOException {
        InputStream inputStream = SendMailService.class.getClassLoader()
                .getResourceAsStream("templates/" + filename);
        return readFromInputStream(inputStream);
    }
}
